const WEEKS = ["Week 1", "Week 2", "Week 3", "Week 4"]
const DAYS  = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"]
// ── END OF FILL IN SECTION ─────────────────────────────────────


function App() {
  const [isManager, setIsManager]               = useState(false)
  const [selectedSalesman, setSelectedSalesman] = useState("")
  const [selectedWeek, setSelectedWeek]         = useState("")
  const [selectedDay, setSelectedDay]           = useState("")
  const [selectedStore, setSelectedStore]       = useState("")
  const [selectedBrand, setSelectedBrand]       = useState("")
  const [comments, setComments]                 = useState("")
  const [products, setProducts]                 = useState({})
  const [submittedStores, setSubmittedStores]   = useState([])
  const [toast, setToast]                       = useState(false)
  const [loading, setLoading]                   = useState(false)
  const [managerSalesman, setManagerSalesman] = useState("")
  const [managerWeek, setManagerWeek]         = useState("")
  const [managerDay, setManagerDay]           = useState("")
  const [visitedStores, setVisitedStores]     = useState([])
  const [managerLoading, setManagerLoading]   = useState(false)
  


  function handleProductToggle(brand, skuName) {
    const key = `${brand}||${skuName}`
    setProducts({
      ...products,
      [key]: !products[key]
    })
  }
  async function fetchVisitedStores() {
    if (!managerSalesman || !managerWeek || !managerDay) return
  
    setManagerLoading(true)
  
    // fetch all submissions for this salesman + week + day
    const { data, error } = await supabase
      .from("submissions")
      .select("store")
      .eq("salesman", managerSalesman)
      .eq("week", managerWeek)
      .eq("day", managerDay)
  
    setManagerLoading(false)
  
    if (error) {
      alert("Error fetching data: " + error.message)
      return
    }
  
    // extract just the store names into an array
    const visited = data.map((row) => row.store)
    setVisitedStores(visited)
  }


  // now looks up stores using salesman + week + day
  function getStoresForDay() {
    if (!selectedSalesman || !selectedWeek || !selectedDay) return []
    return SCHEDULE[selectedSalesman]?.[selectedWeek]?.[selectedDay] || []
  }


  function handleSalesmanChange(name) {
    setSelectedSalesman(name)
    setSubmittedStores([]) 
    setSelectedWeek("")
    setSelectedDay("")
    setSelectedStore("")
    setSelectedBrand("")
    setProducts({})
  }


  function handleWeekChange(week) {
    setSelectedWeek(week)
    setSelectedDay("")
    setSelectedStore("")
    setSelectedBrand("")
    setProducts({})
  }


  function handleDayChange(day) {
    setSelectedDay(day)
    setSelectedStore("")
    setSelectedBrand("")
    setProducts({})
    setSubmittedStores([]) 
  }


  function handleStoreChange(store) {
    setSelectedStore(store)
    setSelectedBrand("")
    setProducts({})
  }


  async function handleSubmit() {
    setLoading(true)
  
    const today = new Date().toLocaleDateString("en-GB")
  
    // build one object where each brand name is a key
    // and its value is a comma separated string of checked SKUs
    const brandColumns = {}
Object.entries(PRODUCTS).forEach(([brand, skus]) => {
  const checkedSkus = skus.filter((sku) => !!products[`${brand}||${sku}`])
  brandColumns[brand] = checkedSkus.join(", ")
})
  
    // the final row looks like:
    // { salesman: "Ahmed", week: "Week 1", day: "Monday", date: "18/04/2026",
    //   store: "Store 1", "Brand 1": "SKU 1A, SKU 1C", "Brand 2": "", "Brand 3": "SKU 3B" }
    const row = {
      salesman: selectedSalesman,
      week:     selectedWeek,
      day:      selectedDay,
      date:     today,
      store:    selectedStore,
      comments: comments,  
      ...brandColumns,  // spreads all brand columns into the row
    }
  
    const { error } = await supabase.from("submissions").insert([row])
  
    setLoading(false)
  
    if (error) {
      alert("Something went wrong: " + error.message)
      return
    }
  
    setToast(true)
    setTimeout(() => setToast(false), 3000)
    setSubmittedStores([...submittedStores, selectedStore])
    setSelectedStore("")
    setSelectedBrand("")
    setProducts({})
    setComments("")
  }


  const availableStores = getStoresForDay()
  const brandNames      = Object.keys(PRODUCTS)


  return (
    <>
      {/* ── HEADER ── */}
      <header className="header">
        <img src={logo} alt="Baqer Mohebi Enterprises" />
        <div className="header-divider" />
        <span className="header-title">Store Tracker</span>
        <button
  onClick={() => {
    if (isManager) {
      setIsManager(false)
    } else {
      const password = prompt("Enter manager password:")
      if (password === "siman") {
        setIsManager(true)
      } else if (password !== null) {
        alert("Incorrect password")
      }
    }
  }}
  style={{
    marginLeft: "auto",
    background: isManager ? "var(--red)" : "var(--surface-2)",
    border: "1px solid var(--border)",
    color: isManager ? "#fff" : "var(--muted)",
    padding: "7px 16px",
    borderRadius: "6px",
    fontFamily: "Barlow, sans-serif",
    fontSize: "13px",
    fontWeight: "600",
    cursor: "pointer",
  }}
>
  {isManager ? "Exit" : "Management Access"}
</button>
      </header>

      <main className="page">

        {/* ── STEP 1: SALESMAN ── */}
        <div className="card">
          <div className="step-label">Step 1 — Who are you?</div>
          <label>Select your name</label>
          <select
            value={selectedSalesman}
            onChange={(e) => handleSalesmanChange(e.target.value)}
          >
            <option value="">— Select name —</option>
            {SALESMEN.map((name) => (
              <option key={name} value={name}>{name}</option>
            ))}
          </select>
        </div>


        {/* ── STEP 2: WEEK ── */}
        {selectedSalesman && (
          <div className="card">
            <div className="step-label">Step 2 — Which week?</div>
            <label>Select week</label>
            <select
              value={selectedWeek}
              onChange={(e) => handleWeekChange(e.target.value)}
            >
              <option value="">— Select week —</option>
              {WEEKS.map((week) => (
                <option key={week} value={week}>{week}</option>
              ))}
            </select>
          </div>
        )}


        {/* ── STEP 3: DAY ── */}
        {selectedWeek && (
          <div className="card">
            <div className="step-label">Step 3 — What day is it?</div>
            <label>Select day</label>
            <select
              value={selectedDay}
              onChange={(e) => handleDayChange(e.target.value)}
            >
              <option value="">— Select day —</option>
              {DAYS.map((day) => (
                <option key={day} value={day}>{day}</option>
              ))}
            </select>
          </div>
        )}

        {selectedDay && submittedStores.length > 0 && (
          <div className="card">
            <div className="step-label">Submitted today</div>
            {submittedStores.map((store, index) => (
              <div
                key={index}
                style={{
                  display: "flex",
                  alignItems: "center",
                  gap: "10px",
                  padding: "8px 0",
                  borderBottom: "1px solid var(--border)",
                  fontSize: "13px",
                  color: "#6fcf6f",
                }}
              >
                <svg width="14" height="14" viewBox="0 0 10 8" fill="none">
                  <path d="M1 4L3.5 6.5L9 1" stroke="#6fcf6f" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round"/>
                </svg>
                {store}
              </div>
            ))}
          </div>
        )}
        {/* ── STEP 4: STORE ── */}
        {selectedDay && availableStores.length > 0 && (
          <div className="card">
            <div className="step-label">Step 4 — Which store?</div>
            <label>Select store</label>
            <select
              value={selectedStore}
              onChange={(e) => handleStoreChange(e.target.value)}
            >
              <option value="">— Select store —</option>
              {availableStores.map((store) => (
                <option key={store} value={store}>{store}</option>
              ))}
            </select>
          </div>
        )}

        {/* no stores assigned */}
        {selectedDay && availableStores.length === 0 && (
          <div className="card">
            <p className="no-stores">No stores assigned for this day.</p>
          </div>
        )}


        {/* ── STEP 5: BRANDS + SKUs ── */}
        {selectedStore && (
          <div className="card">
            <div className="step-label">Step 5 — Products</div>

            {/* brand pill tabs */}
            <div className="brand-tabs">
              {brandNames.map((brand) => {
                const checkedInBrand = PRODUCTS[brand].filter(
                  sku => !!products[`${brand}||${sku}`]
                ).length

                const isActive  = selectedBrand === brand
const hasChecks = checkedInBrand > 0
return (
  <button
    key={brand}
    className={`brand-tab ${isActive ? "active" : ""} ${hasChecks ? "has-checks" : ""}`}
    onClick={() => setSelectedBrand(brand)}
  >
    {brand}
    {hasChecks && (
      <span className="brand-tab-badge">{checkedInBrand}</span>
    )}
  </button>
)
              })}
            </div>

            {/* SKUs for selected brand */}
            {selectedBrand && (
              <>
                <div className="products-header">
                  <span className="products-title">{selectedBrand}</span>
                  <span className="products-count">
                  {PRODUCTS[selectedBrand].filter(sku => !!products[`${selectedBrand}||${sku}`]).length} / {PRODUCTS[selectedBrand].length} checked
                  </span>
                </div>

                <div className="products-grid">
  {PRODUCTS[selectedBrand].map((sku) => {
    const key = `${selectedBrand}||${sku}`
    return (
      <label
        key={key}
        className={`product-item ${products[key] ? "checked" : ""}`}
      >
        <input
          type="checkbox"
          checked={!!products[key]}
          onChange={() => handleProductToggle(selectedBrand, sku)}
        />
        <div className="custom-check">
          <svg width="10" height="8" viewBox="0 0 10 8" fill="none">
            <path d="M1 4L3.5 6.5L9 1" stroke="white" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round"/>
          </svg>
        </div>
        <span className="product-name">{sku}</span>
      </label>
    )
  })}
</div>
              </>
            )}
<div style={{ marginBottom: "16px" }}>
  <label style={{ marginBottom: "8px", display: "block" }}>
    Comments (optional)
  </label>
  <textarea
    value={comments}
    onChange={(e) => setComments(e.target.value)}
    placeholder="Add any notes about this store visit..."
    rows={3}
    style={{
      width: "100%",
      background: "var(--surface-2)",
      color: "var(--text)",
      border: "1px solid var(--border)",
      borderRadius: "6px",
      padding: "11px 14px",
      fontFamily: "Barlow, sans-serif",
      fontSize: "14px",
      resize: "vertical",
      outline: "none",
    }}
  />
</div>
            <button
              className="submit-btn"
              onClick={handleSubmit}
              disabled={loading}
            >
              {loading ? "Submitting..." : "Submit"}
            </button>

          </div>
        )}

      </main>
      {/* ── MANAGER VIEW ── */}
{isManager && (
  <div style={{
    position: "fixed",
    top: 0, left: 0, right: 0, bottom: 0,
    background: "var(--bg)",
    overflowY: "auto",
    zIndex: 100,
    padding: "24px",
  }}>

    {/* header */}
    <div style={{
      display: "flex",
      alignItems: "center",
      marginBottom: "24px",
      gap: "16px"
    }}>
      <h2 style={{
        fontFamily: "Barlow Condensed, sans-serif",
        fontSize: "22px",
        fontWeight: "700",
        letterSpacing: "0.08em",
        textTransform: "uppercase",
      }}>
        Manager View
      </h2>
      <button
        onClick={() => setIsManager(false)}
        style={{
          marginLeft: "auto",
          background: "var(--surface-2)",
          border: "1px solid var(--border)",
          color: "var(--muted)",
          padding: "7px 16px",
          borderRadius: "6px",
          fontFamily: "Barlow, sans-serif",
          fontSize: "13px",
          cursor: "pointer",
        }}
      >
        Close
      </button>
    </div>

    {/* filters */}
    <div style={{ maxWidth: "560px", margin: "0 auto" }}>
      <div className="card">
        <div className="step-label">Select filters</div>

        <label>Salesman</label>
        <select
          value={managerSalesman}
          onChange={(e) => {
            setManagerSalesman(e.target.value)
            setVisitedStores([])
          }}
          style={{ marginBottom: "12px" }}
        >
          <option value="">— Select salesman —</option>
          {SALESMEN.map((name) => (
            <option key={name} value={name}>{name}</option>
          ))}
        </select>

        <label>Week</label>
        <select
          value={managerWeek}
          onChange={(e) => {
            setManagerWeek(e.target.value)
            setVisitedStores([])
          }}
          style={{ marginBottom: "12px" }}
        >
          <option value="">— Select week —</option>
          {WEEKS.map((week) => (
            <option key={week} value={week}>{week}</option>
          ))}
        </select>

        <label>Day</label>
        <select
          value={managerDay}
          onChange={(e) => {
            setManagerDay(e.target.value)
            setVisitedStores([])
          }}
          style={{ marginBottom: "16px" }}
        >
          <option value="">— Select day —</option>
          {DAYS.map((day) => (
            <option key={day} value={day}>{day}</option>
          ))}
        </select>

        <button
          className="submit-btn"
          onClick={fetchVisitedStores}
          disabled={!managerSalesman || !managerWeek || !managerDay || managerLoading}
        >
          {managerLoading ? "Loading..." : "Check Coverage"}
        </button>
      </div>

      {/* results */}
      {visitedStores.length > 0 && managerSalesman && managerWeek && managerDay && (() => {

        const assigned = SCHEDULE[managerSalesman]?.[managerWeek]?.[managerDay] || []
        const notVisited = assigned.filter(store => !visitedStores.includes(store))
        const visited = assigned.filter(store => visitedStores.includes(store))

        return (
          <>
            {/* summary bar */}
            <div className="card" style={{ display: "flex", gap: "24px" }}>
              <div>
                <div style={{ fontSize: "28px", fontWeight: "700", color: "#6fcf6f" }}>
                  {visited.length}
                </div>
                <div style={{ fontSize: "12px", color: "var(--muted)" }}>Visited</div>
              </div>
              <div>
                <div style={{ fontSize: "28px", fontWeight: "700", color: "var(--red)" }}>
                  {notVisited.length}
                </div>
                <div style={{ fontSize: "12px", color: "var(--muted)" }}>Not Visited</div>
              </div>
              <div>
                <div style={{ fontSize: "28px", fontWeight: "700", color: "var(--text)" }}>
                  {assigned.length}
                </div>
                <div style={{ fontSize: "12px", color: "var(--muted)" }}>Total Assigned</div>
              </div>
            </div>

            {/* not visited list */}
            {notVisited.length > 0 && (
              <div className="card">
                <div className="step-label" style={{ color: "var(--red)" }}>
                  Not Visited ({notVisited.length})
                </div>
                {notVisited.map((store, i) => (
                  <div key={i} style={{
                    padding: "10px 0",
                    borderBottom: "1px solid var(--border)",
                    fontSize: "13px",
                    color: "var(--text)",
                    display: "flex",
                    alignItems: "center",
                    gap: "10px",
                  }}>
                    <div style={{
                      width: "8px", height: "8px",
                      borderRadius: "50%",
                      background: "var(--red)",
                      minWidth: "8px",
                    }} />
                    {store}
                  </div>
                ))}
              </div>
            )}

            {/* visited list */}
            {visited.length > 0 && (
              <div className="card">
                <div className="step-label" style={{ color: "#6fcf6f" }}>
                  Visited ({visited.length})
                </div>
                {visited.map((store, i) => (
                  <div key={i} style={{
                    padding: "10px 0",
                    borderBottom: "1px solid var(--border)",
                    fontSize: "13px",
                    color: "#6fcf6f",
                    display: "flex",
                    alignItems: "center",
                    gap: "10px",
                  }}>
                    <svg width="14" height="14" viewBox="0 0 10 8" fill="none">
                      <path d="M1 4L3.5 6.5L9 1" stroke="#6fcf6f" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round"/>
                    </svg>
                    {store}
                  </div>
                ))}
              </div>
            )}
          </>
        )
      })()}
    </div>
  </div>
)}

      {/* ── SUCCESS TOAST ── */}
      {toast && (
        <div className="toast">✓ Submitted successfully</div>
      )}
    </>
  )
}

export default App